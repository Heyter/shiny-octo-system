include("autorun/evidence_settings.lua")


function SpawnEvidence(ply, attacker, death_pos)
	local evidence = ents.Create("evidence")
	if not IsValid( evidence ) then return end
	evidence:SetKiller(attacker)
	evidence:SetVictim(ply)
	evidence:SetPos(death_pos+Vector(0,0,5))
	evidence:Spawn()
end

hook.Add("DoPlayerDeath","repe_evidence",function( ply, attacker, dmginfo )
	if not IsValid(ply) then return end
	if not IsValid(attacker) then return end
	if attacker == ply then return end
	if not Evidence.Settings.DoWantedPlayersDrop then
		if ply:isWanted() then return end
	end
	local death_pos = ply:GetPos()
	if ply:IsPlayer() and attacker:IsPlayer() then
		SpawnEvidence(ply, attacker, death_pos)
	end
end)
